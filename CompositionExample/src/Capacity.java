
public class Capacity {
	private String wattCapacity;
	private String voltCapacity;
	
	public Capacity(String wattCapacity, String voltCapacity) {
		this.wattCapacity = wattCapacity;
		this.voltCapacity = voltCapacity;
	}

	public String getWattCapacity() {
		return wattCapacity;
	}

	public String getVoltCapacity() {
		return voltCapacity;
	}
	

}
