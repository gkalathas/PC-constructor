
public class graphicsSize {
	private int memoryCapacity;
	private int goudaCoreCpacity;
	private int transistorCapacity;
	
	public graphicsSize(int memoryCapacity, int goudaCoreCpacity, int transistorCapacity) {
		this.memoryCapacity = memoryCapacity;
		this.goudaCoreCpacity = goudaCoreCpacity;
		this.transistorCapacity = transistorCapacity;
	}

	public int getMemoryCapacity() {
		return memoryCapacity;
	}

	public int getGoudaCoreCpacity() {
		return goudaCoreCpacity;
	}

	public int getTransistorCapacity() {
		return transistorCapacity;
	}
	
	
	

}